--CREATE A STORED PROCEDURE TO TAKE IN DEPT_ID AND INCREASE EACH EMPLOYEE'S SALARY WITHIN THE 
--DEPARTMENT BY 10%		
CREATE OR REPLACE PROCEDURE SP_GIVE_RAISE (DEPT_ID NUMBER)
AS	
BEGIN

	SELECT DEPARTMENT_NAME, SALARY 
	FROM EMPLOYEE, DEPARTMENT
	AVG(SALARY)

END SP_GIVE_RAISE;

			