--CREATE TWO TABLES: EMPLOYEE
CREATE TABLE EMPLOYEE(EMPLOYEE_ID NUMBER, EMP_FIRSTNAME VARCHAR2,
						EMP_LASTNAME VARCHAR2, DEPARTMENT_ID NUMBER,
						SALARY NUMBER, EMP_EMAIL VARCHAR2
						CONSTRAINT EMPLOYEE_PK PRIMARY KEY (EMPLOYEE_ID));
--CREATE TWO TABLES: DEPARTMENT
CREATE TABLE DEPARTMENT(DEPARTMENT_ID NUMBER, DEPARTMENT_NAME STRING
						CONSTRAINT DEPARTMENT_PK PRIMARY KEY (DEPARTMENT_ID));

--SEQUENCE TO AUTO-GENERATE PRIMARY KEY VALUES FOR EMPLOYEE TABLE
CREATE SEQUENCE employee_seq
MINVALUE 1
MAXVALUE 1000
START WITH 1
INCREMENT BY 1
CACHE 100;

--SEQUENCE TO AUTO-GENERATE PRIMARY KEY VALUES FOR DEPARTMENT TABLE
CREATE SEQUENCE department_seq
MIN VALUE 1
MAXVALUE 50
START WITH 1
INCREMENT BY 1
CACHE 100;

