
function mouseOver() {
    document.getElementById("a0").style.color = "blue";
}

function mouseOut() {
    document.getElementById("a0").style.color = "grey";
}

document.getElementById("a0").addEventListener("mouseover",mouseOver);
document.getElementById("a0").addEventListener("mouseout",mouseOut);

